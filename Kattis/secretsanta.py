def main():
    n = int(input())
    if n == 2:
        print(0.5)
    elif n == 3:
        print(0.66666667)
    elif n == 4:
        print(0.625)
    elif n == 5:
        print(0.63333333)
    elif n == 6:
        print(0.63194444)
    elif n == 7:
        print(0.63214285)
    elif n == 1:
        print(1)
    elif n == 8:
        print(0.63211805)
    else:
        print(0.6321208)


if __name__ == "__main__":
    main()

